public class LoopArrayApp {

    public static void main(String[] args) {
        
        // egoing, glgl, zmfmfm
        String[] user = new String[3];
        user[0] = "egoing";
        user[1] = "glgl";
        user[2] = "zmfmfm";

        for (int i = 0; i < user.length; i++) {
            System.out.println("<li>"+user[i]+"</li>");

        }




    }
}
